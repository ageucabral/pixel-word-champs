
import { useState } from 'react';
import { useToast } from "@/hooks/use-toast";
import { customCompetitionService } from '@/services/customCompetitionService';
import { getCurrentBrasiliaTime } from '@/utils/brasiliaTimeUnified';
import { logger } from '@/utils/logger';

interface DailyCompetition {
  id: string;
  title: string;
  description: string;
  start_date: string;
  end_date: string;
  status: string;
  prize_pool: number;
  max_participants: number;
  total_participants: number;
  theme: string;
  rules: any;
}

export const useDailyCompetitionsActions = () => {
  const { toast } = useToast();
  const [deletingId, setDeletingId] = useState<string | null>(null);

  const handleDelete = async (competition: DailyCompetition, onRefresh?: () => void) => {
    logger.info('üóëÔ∏è Tentando excluir competi√ß√£o di√°ria:', { 
      competitionId: competition.id,
      timestamp: getCurrentBrasiliaTime()
    }, 'USE_DAILY_COMPETITIONS_ACTIONS');
    
    const confirmDelete = window.confirm(`Tem certeza que deseja excluir a competi√ß√£o "${competition.title}"?`);
    if (!confirmDelete) {
      logger.info('‚ùå Exclus√£o cancelada pelo usu√°rio', { 
        timestamp: getCurrentBrasiliaTime()
      }, 'USE_DAILY_COMPETITIONS_ACTIONS');
      return;
    }

    setDeletingId(competition.id);
    
    try {
      logger.info('üì§ Chamando servi√ßo de exclus√£o...', { 
        timestamp: getCurrentBrasiliaTime()
      }, 'USE_DAILY_COMPETITIONS_ACTIONS');
      const response = await customCompetitionService.deleteCompetition(competition.id);
      
      if (response.success) {
        logger.info('‚úÖ Competi√ß√£o exclu√≠da com sucesso', { 
          timestamp: getCurrentBrasiliaTime()
        }, 'USE_DAILY_COMPETITIONS_ACTIONS');
        toast({
          title: "Competi√ß√£o exclu√≠da",
          description: `A competi√ß√£o "${competition.title}" foi exclu√≠da com sucesso.`,
        });
        
        if (onRefresh) {
          logger.info('üîÑ Atualizando lista de competi√ß√µes...', { 
            timestamp: getCurrentBrasiliaTime()
          }, 'USE_DAILY_COMPETITIONS_ACTIONS');
          onRefresh();
        }
      } else {
        logger.error('‚ùå Erro no servi√ßo:', { 
          error: response.error,
          timestamp: getCurrentBrasiliaTime()
        }, 'USE_DAILY_COMPETITIONS_ACTIONS');
        throw new Error(response.error || 'Erro ao excluir competi√ß√£o');
      }
    } catch (error) {
      logger.error('‚ùå Erro ao excluir competi√ß√£o:', { 
        error,
        timestamp: getCurrentBrasiliaTime()
      }, 'USE_DAILY_COMPETITIONS_ACTIONS');
      toast({
        title: "Erro ao excluir",
        description: error instanceof Error ? error.message : "N√£o foi poss√≠vel excluir a competi√ß√£o. Tente novamente.",
        variant: "destructive",
      });
    } finally {
      setDeletingId(null);
    }
  };

  return {
    deletingId,
    handleDelete
  };
};
